package com.buy.together.domain;

public class Admin {

	private int admin_number;
	private String admin_id;
	private String admin_pw;
	private String admin_nickname;
	private String admin_email;
	private String admin_phoneNumber;
	private int admin_grade;

	public Admin() {

	}

	public int getAdmin_number() {
		return admin_number;
	}

	public void setAdmin_number(int admin_number) {
		this.admin_number = admin_number;
	}

	public String getAdmin_id() {
		return admin_id;
	}

	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}

	public String getAdmin_pw() {
		return admin_pw;
	}

	public void setAdmin_pw(String admin_pw) {
		this.admin_pw = admin_pw;
	}

	public String getAdmin_nickname() {
		return admin_nickname;
	}

	public void setAdmin_nickname(String admin_nickname) {
		this.admin_nickname = admin_nickname;
	}

	public String getAdmin_email() {
		return admin_email;
	}

	public void setAdmin_email(String admin_email) {
		this.admin_email = admin_email;
	}

	public String getAdmin_phoneNumber() {
		return admin_phoneNumber;
	}

	public void setAdmin_phoneNumber(String admin_phoneNumber) {
		this.admin_phoneNumber = admin_phoneNumber;
	}

	public int getAdmin_grade() {
		return admin_grade;
	}

	public void setAdmin_grade(int admin_grade) {
		this.admin_grade = admin_grade;
	}

}
